# Шаблоны сообщений по умолчанию для каждого уровня
DEFAULT_PROMPTS = {
    "primary": "Для {grade} класса начальной школы. Учитывай возрастные особенности, используй игровые формы, наглядность.",
    "middle": "Для {grade} класса средней школы. Учитывай программу ФГОС, межпредметные связи, подготовку к ОГЭ.",
    "senior": "Для {grade} класса старшей школы. Профильный уровень, углублённая подготовка, ориентация на вуз."
}


def get_subject_prompt(subject: str, grade: str) -> str:
    """
    Возвращает специфичный промт для предмета с учётом класса.
    
    Args:
        subject: Название предмета
        grade: Номер класса (строка)
    
    Returns:
        Промт для генерации материалов по предмету
    """
    grade_num = int(grade)
    
    # Начальная школа (1-4 класс)
    if grade_num <= 4:
        base_prompt = PRIMARY_PROMPTS.get(subject)
        if base_prompt:
            return f"Для {grade} класса начальной школы. {base_prompt}"
        return DEFAULT_PROMPTS["primary"].format(grade=grade)
    
    # Средняя школа (5-9 класс)
    elif 5 <= grade_num <= 9:
        base_prompt = MIDDLE_PROMPTS.get(subject)
        if base_prompt:
            return f"Для {grade} класса средней школы. {base_prompt}"
        return DEFAULT_PROMPTS["middle"].format(grade=grade)
    
    # Старшая школа (10-11 класс)
    else:
        base_prompt = SENIOR_PROMPTS.get(subject)
        if base_prompt:
            return f"Для {grade} класса старшей школы. {base_prompt}"
        return DEFAULT_PROMPTS["senior"].format(grade=grade)
